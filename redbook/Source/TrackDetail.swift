import SwiftUI

struct TrackDetail: View {
  
  let trackNumber: Int
  let trackArtist: String
  let trackName: String
  let trackDuration: String
  let sourceArtist: String
  
  var body: some View {
    // TODO: lots of view formatting here, is it needed?
    HStack {
      Text(String(trackNumber))
        .font(.footnote)
        .frame(width: 20, alignment: .center)
        .padding(.vertical)
        .padding(.trailing)
      VStack(alignment: .leading) {
        Text(trackName)
          .font(.callout)
          .fontWeight(.medium)
          .lineLimit(1)
        if trackArtist != sourceArtist {
          Text(trackArtist)
            .font(.callout)
            .fontWeight(.light)
            .opacity(0.7)
            .lineLimit(1)
        }
      }
      Spacer()
      Text(trackDuration)
        .font(.footnote)
        .opacity(0.7)
    }
  }
}
